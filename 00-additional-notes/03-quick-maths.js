// Time for quick maths!
// you can increment a number in this way

let a = 10;
a = a + 1;

// Or you can do it this way!
let b = 10;
b++;

// b++ is just shorthand for b = b + 1
// so ++b

// we’ll cover how they defer in another example for now it’s cool to know that :)

b--; // b = b -1;
--b; // also b = b -1



// We can even do for more than just an interval of 1!

let c = 20;
c += 10; // c = c + 10;
// or

c -= 10; // c = c - 10

